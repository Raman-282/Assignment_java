package HashSet;
import java.util.*;

public class NumberOfElements {
	public static void main(String args[]) {
		//create a hash set
				HashSet<String> h_set = new HashSet<String>();
				h_set.add("Raman");
				h_set.add("Vivek");
				h_set.add("Abhinav");
				h_set.add("Sahil");
				//print the hash set
				System.out.println("Size of the Hash Set:"+h_set.size());
	}

}
