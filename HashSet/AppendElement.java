package HashSet;
import java.util.HashSet;

public class AppendElement {
	public static void main(String args[]) {
		//create a hash set
		HashSet<String> h_set = new HashSet<String>();
		h_set.add("Raman");
		h_set.add("Vivek");
		h_set.add("Abhinav");
		h_set.add("Sahil");
		//print the hash set
		System.out.println("The Hash set:"+h_set);
		
	}

}
