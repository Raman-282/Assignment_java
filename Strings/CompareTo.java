package Strings;

public class CompareTo {
public static void main(String args[]) {
		
		String s1="Hi";
		String s2="Hi";
		String s3="good";
		String s4="average";
		System.out.println(s1.compareTo(s2));
		System.out.println(s1.compareTo(s3));
		System.out.println(s1.compareTo(s4));
		System.out.println(s3.compareTo(s4));
	}


}


